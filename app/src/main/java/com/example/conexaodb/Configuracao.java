package com.example.conexaodb;

public class Configuracao {
	private String macAdress;
	private int maxGrafico;
	private int minGrafico;
	private int stepGrafico;
	private int taxAmostragem;
	private double coefAngular;
	private double coefLinear;
	public String getMacAdress() {
		return macAdress;
	}
	public void setMacAdress(String macAdress) {
		this.macAdress = macAdress;
	}
	public int getMaxGrafico() {
		return maxGrafico;
	}
	public void setMaxGrafico(int maxGrafico) {
		this.maxGrafico = maxGrafico;
	}
	public int getMinGrafico() {
		return minGrafico;
	}
	public void setMinGrafico(int minGrafico) {
		this.minGrafico = minGrafico;
	}
	public int getStepGrafico() {
		return stepGrafico;
	}
	public void setStepGrafico(int stepGrafico) {
		this.stepGrafico = stepGrafico;
	}
	public int getTaxAmostragem() {
		return taxAmostragem;
	}
	public void setTaxAmostragem(int taxAmostragem) {
		this.taxAmostragem = taxAmostragem;
	}
	public double getCoefAngular() {
		return coefAngular;
	}
	public void setCoefAngular(double coefAngular) {
		this.coefAngular = coefAngular;
	}
	public double getCoefLinear() {
		return coefLinear;
	}
	public void setCoefLinear(double coefLinear) {
		this.coefLinear = coefLinear;
	}

	
}
	
	